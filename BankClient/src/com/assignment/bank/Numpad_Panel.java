    void get_val()
    {
        Integer multiplier = 1;
        val = 0;
        Integer[] in_val = Numpad_Panel.in.toArray(new Integer[Numpad_Panel.in.size()]);
        for (int i = Numpad_Panel.in.size()-1; i>=0; i--)
        {
            val = val + (in_val[i] * multiplier);
            multiplier = multiplier * 10;
        }
    }
    
