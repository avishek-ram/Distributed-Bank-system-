    void input()
    {
        
        switch (Main_Driver.opt) {    
            case "pin_ck":
                disp_in = "<html><p style=\"text-align:center;\">";
                in.forEach((_item) -> {
                    disp_in = disp_in + " /* ";
                }); break;
            case "acc_ck":
            case "depo":
            case "with":
            case "pay":
                if (in.isEmpty())
                {
                    disp_in = "";
                }
                else
                {
                    get_val();
                    disp_in = ""+val;
                }
                break;
            default:
        }
        
        info_panel.setText(in_disp+disp_in);
    }
    
